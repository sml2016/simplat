        <p><span class="CapitalLetter">M</span>aru Mora Villalpando llegó a los Estados Unidos en 1992, desde México, asustada por la violencia desatada en su país por la inestabilidad política. Su experiencia como indocumentada tras más de 20 años en Seattle la llevó a dedicar su vida a ayudar a otros en su situación.</p>
        <p>Su misión es ayudar a los latinos que enfrentan casos de inmigración, en especial educarlos sobre cómo navegar el sistema de inmigración en los Estados Unidos. Latino Advocacy, la organización fundada por Mora Villalpando también ayuda a la comunidad latina a organizarse mediante la consultoría y capacitación de grupos enfocados tanto en la justicia civil como en derechos de inmigrantes en el estado de Washington. 
        Maru Mora Villapaldo no necesitó un título universitario para liderar a la comunidad de inmigrantes latinos contra las deportaciones, tampoco para organizar protestas frente al Centro de Detención del Noroeste en Tacoma, Washington. Su rol como organizadora y líder de la comunidad está inspirado en su experiencia como indocumentada en los Estados Unidos. </p> 
        <p>Hace 15 años, tras el nacimiento de su hija Josefina Mora, empezó a informarse sobre sus derechos migratorios y comprometiéndose activamente contra el racismo e intolerancia que muchos inmigrantes enfrenta. “Quería aprender sobre mis derechos, conectar con otros miembros de la comunidad indocumentada y comenzar a crear una coalición que exigiera que se nos trate como seres humanos”, explicó en un artículo publicado en 2015.</p>
        <p>Su rol como luchadora y defensora de los Derechos Humanos de los inmigrantes latinos en Estados Unidos es reconocido en todo el continente americano. Su liderazgo como vocera de la campaña ‘Ni una Deportación Más’ es una muestra de lo que las mujeres latinas pueden hacer cuando conocen sus derechos y se sienten seguras de si mismas. </p>  
